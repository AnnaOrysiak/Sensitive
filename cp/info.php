<h2>Wskazówki</h2>
<div style="text-align: justify;">1. Aby rozpocząć edycję, wybierz opcję z listy.<br />
2.a. Wciśnięcie klawisza "Enter" skutkuje przeniesieniem do nowej linii z zastosowaniem wcięcia.<br />
2.b. Przeniesienie do nowej linii bez wcięcia wywołujemy kombinacją "Shift" + "Enter".<br />
3. Aby wkleić gotową treść do edytora, wciśnij najpierw przycisk "Pokaż źródło",  a następnie wklej tekst, po czym wróć do trybu edycji.<br />
4. Przed wysłaniem zedytowanego dzieła (na wszelki wypadek) skopiuj jego źródło.<br />
5. Jeśli podczas edycji wystąpił błąd, którego nie możesz poprawić samodzielnie - napisz do admina.<br />
6. Masz pomysł lub sugestię? - Napisz do admina.<br />
